So, if you write =E2=80=9Cthe Earth=E2=80=9D for the planet, you are alread=
y swimming against the current of established English language conventions.

But this isn't just about style=C2=B2. This is ontological. The way we writ=
e 'Earth' betrays and perpetuates a deep corruption of thought with dire ec=
ological consequences.

Think for a moment about how we talk about the other planets in our solar s=
ystem.

Do you say, =E2=80=9CI=E2=80=99d love to visit the Mars=E2=80=9D?

