the tiny building blocks of sentences, paragraphs, stories, and visions=E2=
=80=94if corrupted, can manifest cities and worlds of great woe.

While Plato and Camus had societal woe in mind, as we know too well today, =
there=E2=80=99s also the ecological sort: pollution, ecological degradation=
, and biological depletion.

For the last ten years, my colleague Banayan and I have been reflecting on =
this kind of woe and where it comes from. We've been working hard on plasti=
c pollution from the perspective of her people=E2=80=94the Igorots, indigen=
ous people of Northern Luzon in the West Philippine Sea. They have managed =
to retain their culture despite three hundred years of colonizing attempts =
by the Spanish, Japanese, and Americans. As we've worked on essays together=
, I've come to realize that the Igorots (like so many other kincentric cult=
ures) retain a dazzlingly different view of the world than modern cultures.

Working with her on refining paragraphs and sentences, I=E2=80=99ve become =
particularly conscious of the problematic modern words in my own English vo=
cabulary.

Perhaps the most egregious is the way many of us write the name of our home=
 planet: Earth.

Specifically, the third rock from the Sun.

Specificity is key here. The word =E2=80=9Cearth=E2=80=9D has several disam=
biguations. For example, when we refer to soil or the ground=E2=80=94or eve=
n tectonic plates=E2=80=94we speak of "the earth". For example, "the earth =
beneath my feet." Notice the article and the lack of capitalization. Here, =
the sentence is not referring to our planet but rather an aspect of its sur=
face. This use of the word earth is not an issue.

Writing of our planet Earth, however, is another matter.

Alas, all too often, well-intentioned environmentalists and Earth-lovers re=
fer to "the Earth" when they are talking about the planet.

For example, Bill Gates writes: "The earth is warming, it=E2=80=99s warming=
 because of human activity, and the impact is bad and will get much worse" =
(2015, How to Solve a Climate Crisis).

Another example: =E2=80=9CReconnecting with the Earth and each other=E2=80=
=9D (Bioneers slogan).

Or: "We would seek to preserve the sanctity of life and replenish the Earth=
=E2=80=99s natural systems..." (Daniel Pinchbeck, Foundation).

First, let's be clear, these are all categorical stylistic mistakes.

The AP Stylebook [https://earthen.io/r/395d6d0b?m=3D4dbbb711-73e9-4fd0-9056=
-a7cc1af6a905] and the MLA guide [https://earthen.io/r/c3f5759c?m=3D4dbbb71=
1-73e9-4fd0-9056-a7cc1af6a905] stand against the use of the article the whe=
n referring to the planet Earth (though slightly lukewarm in their emphasis=
*). However, the UN style guide [https://earthen.io/r/c490a7c8?m=3D4dbbb711=
-73e9-4fd0-9056-a7cc1af6a905], and the American Astronomical Society, Space=
=2Ecom [https://earthen.io/r/74258bba?=
