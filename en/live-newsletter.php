you simply say her name. Likewise, a mountain, a river, a forest is address=
ed as someone, not something.

Learning from them, we must revisit not only our actions but our assumption=
s=E2=80=93 the integral transition of our age. And that begins with how we =
write and how we name the unique cosmological entity that is our common hom=
e.

Perhaps the measure of how important this transition is can be seen in how =
difficult it is to make.

In my own writing, I=E2=80=99ve been striving to excise =E2=80=9Cthe=E2=80=
=9D from Earth for the last year! Despite reflecting on the importance of t=
his change, developing an ethical theory around it, and writing this very e=
ssay, I constantly find myself correcting my inadvertent Earth objectificat=
ions.

I am sure you will too.

All the more reason to be gentle with those who make the mistake. The deepl=
y ingrained tendency to write this way points to just how embedded the obje=
ctification of Earth is in our modern way of seeing the world.

However, there is no room for ambiguity.

Not only is the articulation and objectification of Earth stylistically fla=
wed, it belies an insidious misconception. For ancient and ongoing kincentr=
ic cultures the objectification of Earth and her entities is both absent in=
 their language and anathema in their ontology.

To move forward, we must first thoroughly excise our objectification of pla=
net Earth from our vocabulary. Only then can we shatter the mind-forged man=
acles of antiquated, materialist cosmology and open the door to the ecologi=
cal regeneration to which our moment so urgently calls.

Here is the door.

Once we remove =E2=80=9Cthe=E2=80=9D from =E2=80=9CEarth=E2=80=9D, it is th=
at much easier to remove it when we speak of Bear, of Salmon, of Eagle, of =
Whale, and whatever other kindred creatures share space with you. And once =
we do, their commodification, exploitation, and depletion become that much =
more difficult and inconceivable.

Furthermore, when we name Earth without an article, we name a presence rath=
er than an object. We honor a cosmological entity that has, for billions of=
 years, concentrated matter, dispersed energy, increased biodiversity, and =
raised awareness. And with that, we can we at last see Earth as a teacher=
=E2=80=94an example that we, as Earthlings, can and should follow [https://=
earthen.io/r/0c4e2d1d?m=3D4dbbb711-73e9-4fd0-9056-a7cc1af6a905].

Only then, as our language re-aligns and harmonizes with ecological and ont=
ological reality, so too can we.

It is synchronization that must begin at the root=E2=80=93 with our words. =
In this small but sovereign act of re-naming, our sentences change, our sto=
